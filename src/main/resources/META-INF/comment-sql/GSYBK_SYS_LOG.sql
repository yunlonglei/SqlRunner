CREATE TABLE SYS_LOG,
(
    ID VARCHAR2(255 char) PRIMARY KEY NOT NULL,
    CREATE_BY VARCHAR2(255 char),
    CREATE_TIME TIMESTAMP(6),
    DESTROY_TIME TIMESTAMP(6),
    MODIFY_TIME TIMESTAMP(6),
    UPDATE_BY VARCHAR2(255 char),
    VERSION NUMBER(10),
    BODY CLOB,
    CLASS_ANNOTATION VARCHAR2(255 char),
    CODE NUMBER(10),
    EXCEPTIONS CLOB,
    HEADER CLOB,
    IP VARCHAR2(255 char),
    METHOD VARCHAR2(255 char),
    METHOD_ANNOTATION VARCHAR2(255 char),
    PARAMETERS CLOB,
    REQUEST_URL CLOB,
    RESULT CLOB,
    TIME NUMBER(19) NOT NULL,
    USER_NAME VARCHAR2(255 char)
);